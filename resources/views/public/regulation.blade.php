<div class="container my-5">
    <h1 class="text-center mb-4">Regulamin Serwisu Edukacyjnego "Testostrefa"</h1>

    <section>
        <h2>§1 Postanowienia ogólne</h2>
        <p>1. Niniejszy regulamin określa zasady korzystania z serwisu internetowego "Testostrefa", umożliwiającego tworzenie, udostępnianie oraz rozwiązywanie quizów i zestawów pytań.</p>
        <p>2. Operatorem serwisu jest admin@testostrefa.pl</p>
        <p>3. Rejestracja i korzystanie z serwisu oznacza akceptację niniejszego regulaminu.</p>
        <p>4. Serwis jest przeznaczony dla użytkowników pełnoletnich zgodnie z obowiązującym prawem polskim.</p>
        <p>5. Korzystanie z serwisu jest darmowe; nie są pobierane żadne opłaty ani nie ma planów subskrypcyjnych.</p>
    </section>

    <section>
        <h2>§2 Role użytkowników</h2>
        <p>1. Serwis udostępnia trzy role użytkowników:</p>
        <ul>
            <li><strong>Administrator</strong>: posiada pełny dostęp do wszystkich systemów i funkcjonalności.</li>
            <li><strong>Nauczyciel</strong>: może tworzyć nowe zestawy pytań oraz kursy.</li>
            <li><strong>Uczeń</strong>: ma dostęp tylko do udostępnionych kursów i zestawów pytań.</li>
        </ul>
        <p>2. Każdy użytkownik może posiadać wyłącznie jedno konto; tworzenie multikont jest zabronione.</p>
    </section>

    <section>
        <h2>§3 Tworzenie i udostępnianie treści</h2>
        <p>1. Użytkownicy mogą tworzyć quizy oraz inne materiały, zwane dalej "Treścią", które muszą być zgodne z prawem i regulaminem.</p>
        <p>2. Użytkownik ponosi wyłączną odpowiedzialność za zamieszczoną Treść oraz ewentualne szkody wynikające z jej publikacji.</p>
        <p>3. Użytkownik udziela "Testostrefie" niewyłącznej, wieczystej, obowiązującej na całym świecie, wolnej od opłat (z możliwością udzielania sublicencji) licencji na:</p>
        <ul>
            <li>Używanie, kopiowanie, reprodukowanie, przetwarzanie, adaptowanie, modyfikowanie, publikowanie, transmitowanie, prezentowanie i rozpowszechnianie w dowolnych formatach i metodach Treści udostęnionych przez Użytkownika.</li>
        </ul>
        <p>4. "Testostrefa" nie ponosi odpowiedzialności za Treść zamieszczoną przez użytkowników.</p>
    </section>

    <section>
        <h2>§4 Zakazane treści i działania</h2>
        <p>1. Zabrania się publikowania Treści:</p>
        <ul>
            <li>Zawierającej treści obraźliwe, wulgarne, pornograficzne, nawołujące do przemocy, niezgodne z prawem lub naruszające prawa innych osób.</li>
            <li>Naruszającej prawa autorskie, własności intelektualnej lub dobra osobiste innych osób.</li>
            <li>Zawierającej wirusy, złośliwe oprogramowanie lub inne szkodliwe elementy.</li>
            <li>Wprowadzającej innych użytkowników w błąd (np. fałszywe informacje, oszustwa).</li>
        </ul>
        <p>2. Zabronione działania obejmują:</p>
        <ul>
            <li>Zakłócanie funkcjonowania serwisu, w tym próby włamań, instalowanie szkodliwego oprogramowania czy ataki DDoS.</li>
            <li>Uzyskiwanie nieautoryzowanego dostępu do kont innych użytkowników lub systemów serwisu.</li>
            <li>Wykorzystywanie serwisu do działań niezgodnych z prawem, takich jak phishing czy rozpowszechnianie nielegalnych treści.</li>
            <li>Wprowadzanie automatycznych interakcji z serwisem (np. za pomocą botów) bez zgody operatora.</li>
        </ul>
    </section>

    <section>
        <h2>§5 Oświadczenia i gwarancje dotyczące Treści</h2>
        <p>Udostępniając Treść, użytkownik zapewnia i gwarantuje, że:</p>
        <ul>
            <li>Pobieranie, kopiowanie i użytkowanie Treści nie narusza praw autorskich, patentowych, znaków handlowych ani tajemnic handlowych jakiegokolwiek podmiotu.</li>
            <li>Treść nie zawiera wirusów, złośliwego oprogramowania ani innych destrukcyjnych elementów.</li>
            <li>Treść w pełni przestrzega warunków licencji, jeśli jest oparta na materiałach osób trzecich.</li>
            <li>Treść nie jest spamem ani materiałem komercyjnym służącym do wprowadzania w błąd.</li>
        </ul>
    </section>

    <section>
        <h2>§6 Odpowiedzialność operatora</h2>
        <p>1. "Testostrefa" nie ponosi odpowiedzialności za błędy lub problemy wynikające z:</p>
        <ul>
            <li>Korzystania z urządzeń, oprogramowania lub komponentów niezwiązanych z serwisem.</li>
            <li>Niezastosowania się użytkownika do zaleceń operatora, w tym odrzucenia aktualizacji lub poprawek technicznych.</li>
            <li>Działań lub zaniechań osób trzecich niezwiązanych z operatorem.</li>
        </ul>
        <p>2. Operator dokłada wszelkich starań, aby serwis działał prawidłowo, jednak nie gwarantuje braku błędów czy nieprzerwanych usług.</p>
    </section>

    <section>
        <h2>§7 Prywatność i bezpieczeństwo</h2>
        <p>1. Dane osobowe są przetwarzane zgodnie z Polityką Prywatności, dostępną w osobnym dokumencie.</p>
        <p>2. Użytkownik zobowiązuje się do niezwłocznego zgłaszania naruszeń bezpieczeństwa lub nieuprawnionego dostępu do swojego konta.</p>
    </section>

    <section>
        <h2>§8 Zmiany regulaminu</h2>
        <p>1. Operator zastrzega sobie prawo do zmiany regulaminu.</p>
        <p>2. Użytkownicy zostaną poinformowani o zmianach co najmniej 14 dni przed ich wejściem w życie.</p>
    </section>

    <section>
        <h2>§9 Klauzule końcowe</h2>
        <p>1. Jeżeli jakakolwiek część niniejszego regulaminu zostanie uznana za nieważną, pozostałe jego postanowienia pozostają w mocy.</p>
        <p>2. Niewykonanie lub zaniechanie egzekwowania jakiegokolwiek postanowienia regulaminu przez operatora nie oznacza zrzeczenia się tych praw.</p>
    </section>
