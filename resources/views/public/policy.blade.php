    <h1 class="text-center mb-4">Polityka Prywatności Serwisu "Testostrefa"</h1>

    <section>
        <h2>§1 Wprowadzenie</h2>
        <p>1. Serwis "Testostrefa" szanuje prywatność użytkowników i zobowiązuje się do ochrony ich danych osobowych zgodnie z obowiązującymi przepisami prawa, w tym RODO (Rozporządzeniem Parlamentu Europejskiego i Rady (UE) 2016/679).</p>
        <p>2. Niniejsza Polityka Prywatności określa, jakie dane osobowe są zbierane, wykorzystywane oraz jakie prawa przysługują użytkownikom w związku z ich danymi.</p>
    </section>

    <section>
        <h2>§2 Administrator danych osobowych</h2>
        <p>1. Administratorem danych osobowych jest właściciel serwisu testostrefa.pl.</p>
        <p>2. Kontakt z administratorem jest możliwy za pośrednictwem e-maila: admin@testostrefa.pl.</p>
    </section>

    <section>
        <h2>§3 Jakie dane zbieramy?</h2>
        <p>1. Zbieramy dane osobowe, które użytkownik podaje dobrowolnie podczas rejestracji lub korzystania z serwisu. Może to obejmować:</p>
        <ul>
            <li>Imię i nazwisko</li>
            <li>Adres e-mail</li>
            <li>Inne dane dobrowolnie udostępnione w ramach profilu użytkownika</li>
        </ul>
        <p>2. Automatycznie zbieramy również dane techniczne, takie jak:</p>
        <ul>
            <li>Adres IP</li>
            <li>Rodzaj przeglądarki</li>
            <li>System operacyjny</li>
            <li>Historia odwiedzin w serwisie</li>
            <li>Alerty aplikacji</li>
        </ul>
    </section>

    <section>
        <h2>§4 W jakim celu przetwarzamy dane?</h2>
        <p>Dane osobowe są przetwarzane w następujących celach:</p>
        <ul>
            <li>Zapewnienie prawidłowego działania serwisu</li>
            <li>Tworzenie i udostępnianie quizów oraz zestawów pytań</li>
            <li>Komunikacja z użytkownikami (np. wysyłanie powiadomień o zmianach regulaminu)</li>
            <li>Poprawa jakości usług i analiza statystyczna</li>
            <li>Ochrona przed nadużyciami oraz egzekwowanie regulaminu</li>
        </ul>
    </section>

    <section>
        <h2>§5 Udostępnianie danych</h2>
        <p>1. Dane osobowe mogą być udostępniane podmiotom trzecim wyłącznie w przypadkach:</p>
        <ul>
            <li>Kiedy jest to wymagane przez prawo</li>
            <li>Kiedy jest to konieczne do realizacji usług serwisu (np. usługi hostingowe)</li>
            <li>Kiedy użytkownik wyraził na to zgodę</li>
        </ul>
        <p>2. Serwis nie sprzedaje danych użytkowników podmiotom trzecim.</p>
    </section>

    <section>
        <h2>§6 Przechowywanie danych</h2>
        <p>1. Dane osobowe są przechowywane przez okres niezbędny do realizacji celów, dla których zostały zebrane, lub do momentu usunięcia konta przez użytkownika.</p>
        <p>2. Po upływie okresu przechowywania dane są usuwane lub anonimizowane.</p>
    </section>

    <section>
        <h2>§7 Prawa użytkownika</h2>
        <p>Każdemu użytkownikowi przysługują następujące prawa:</p>
        <ul>
            <li>Prawo dostępu do swoich danych osobowych</li>
            <li>Prawo do sprostowania nieprawidłowych lub niekompletnych danych</li>
            <li>Prawo do usunięcia danych ("prawo do bycia zapomnianym")</li>
            <li>Prawo do ograniczenia przetwarzania</li>
            <li>Prawo do przenoszenia danych</li>
            <li>Prawo do wniesienia sprzeciwu wobec przetwarzania danych</li>
            <li>Prawo do wniesienia skargi do organu nadzorczego</li>
        </ul>
    </section>

    <section>
        <h2>§8 Bezpieczeństwo danych</h2>
        <p>1. Serwis podejmuje odpowiednie środki techniczne i organizacyjne w celu ochrony danych osobowych użytkowników przed nieautoryzowanym dostępem, utratą, zniszczeniem lub modyfikacją.</p>
        <p>2. Użytkownik jest zobowiązany do zachowania poufności swoich danych logowania i nieudostępniania ich osobom trzecim.</p>
    </section>

    <section>
        <h2>§9 Pliki cookies</h2>
        <p>1. Serwis wykorzystuje pliki cookies w celu zapewnienia poprawnego działania serwisu oraz analizy statystycznej.</p>
        <p>2. Użytkownik ma prawo do zarządzania ustawieniami plików cookies w swojej przeglądarce internetowej.</p>
    </section>

    <section>
        <h2>§10 Zmiany Polityki Prywatności</h2>
        <p>1. Zastrzegamy sobie prawo do zmiany niniejszej Polityki Prywatności w dowolnym czasie.</p>
        <p>2. O wszelkich zmianach użytkownicy zostaną poinformowani co najmniej 14 dni przed ich wejściem w życie.</p>
    </section>
